DECLARE
    CURSOR CUR_MHS IS SELECT  MHS.NIM, MHS.NAMA, PDI.NAMA, MK.NAMA,MK.SKS,MK.KODE,KRS.NILAI,JWL.TA,JWL.RUANGAN,DSN.NAMA
    FROM MAHASISWA MHS, PRODI PDI ,MATAKULIAH MK,KRS,JADWAL JWL,DOSEN DSN
    WHERE MHS.PRODI = PDI.KODE
    AND MHS.NIM = KRS.NIM
    AND KRS.KODEMK = MK.KODE
    AND MK.KODE = JWL.KODEMK
    AND JWL.NIP = DSN.NIP
    AND MHS.NIM = '12350114518'
    AND KRS.SEMESTER = 2;
    V_NIM MAHASISWA.NIM%TYPE;
    V_NAMAMHS MAHASISWA.NAMA%TYPE;
    V_PRODI PRODI.NAMA%TYPE;
    V_NAMA MATAKULIAH.NAMA%TYPE;
    V_SKS MATAKULIAH.SKS%TYPE;
    V_KODE MATAKULIAH.KODE%TYPE;
    V_NILAI KRS.NILAI%TYPE;
    v_TA JADWAL.TA%TYPE;
    v_NAMADSN DOSEN.NAMA%TYPE;
    V_RUANGAN JADWAL.RUANGAN%TYPE;
BEGIN
        OPEN CUR_MHS;
            LOOP
                FETCH CUR_MHS INTO V_NIM, V_NAMAMHS, V_PRODI, V_NAMA, V_SKS, V_KODE, V_NILAI, V_TA, v_NAMADSN, v_RUANGAN;
                EXIT WHEN CUR_MHS%NOTFOUND;
                DBMS_OUTPUT.PUT_LINE('NIM MAHASISWA : ' || V_NIM ||  ' ' || 'NAMA MAHASISWA :' || V_NAMAMHS  || ' ' || 'NAMA PRODI : ' || V_PRODI || ' ' || V_NAMA || ' ' || 'SKS :' || V_SKS  || ' ' ||'KODE MATKULIAH :' || V_KODE || ' ' || 'NILAI: ' || V_NILAI || ' ' || 'JADWAL : ' || v_TA || ' ' || 'DOSEN : ' || v_NAMADSN); 
            END LOOP;
        CLOSE CUR_MHS;
END;