DECLARE
    CURSOR CUR_MHS IS SELECT  MHS.NIM, MHS.NAMA NAMAMHS, PDI.NAMA, MK.NAMA NAMAMK,MK.SKS,MK.KODE,KRS.NILAI
    FROM MAHASISWA MHS, PRODI PDI ,MATAKULIAH MK,KRS
    WHERE MHS.PRODI = PDI.KODE
    AND KRS.KODEMK = MK.KODE
    AND KRS.SEMESTER = 2
    AND MHS.NIM = '12350114518';
    V_KRS CUR_MHS%ROWTYPE;
BEGIN
        OPEN CUR_MHS;
            LOOP
                FETCH CUR_MHS INTO V_KRS;
                EXIT WHEN CUR_MHS%ROWCOUNT > 6 OR CUR_MHS%NOTFOUND;
               DBMS_OUTPUT.PUT_LINE('NIM MAHASISWA : ' || V_KRS.NIM ||  ' | NAMA MAHASISWA : ' || V_KRS.NAMAMHS || ' | NAMA PRODI : ' || V_KRS.NAMA || ' | MATAKULIAH : ' || V_KRS.NAMAMK ||' | SKS : ' || V_KRS.SKS  || ' | KODE MATKULIAH : ' || V_KRS.KODE || ' | NILAI : ' || V_KRS.NILAI);
               END LOOP;
        CLOSE CUR_MHS;
END;