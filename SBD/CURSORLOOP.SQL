DECLARE
    CURSOR CUR_MHS IS SELECT  MHS.NIM, MHS.NAMA NAMAMHS, MK.NAMA NAMAMK,MK.SKS,MK.KODE,KRS.NILAI,JWL.TA,JWL.RUANGAN,DSN.NAMA
    FROM MAHASISWA MHS ,MATAKULIAH MK,KRS,JADWAL JWL,DOSEN DSN
    WHERE KRS.NIM = MHS.NIM
    AND KRS.KODEMK = MK.KODE
    AND MK.KODE = JWL.KODEMK
    AND JWL.NIP = DSN.NIP
    AND KRS.SEMESTER = 2
    AND MHS.NIM = '12350114518';
BEGIN
            FOR V_KRS IN CUR_MHS LOOP
               DBMS_OUTPUT.PUT_LINE('NIM MAHASISWA : ' || V_KRS.NIM ||  ' | NAMA MAHASISWA : ' || V_KRS.NAMAMHS || ' | MATAKULIAH : ' || V_KRS.NAMAMK ||' | SKS : ' || V_KRS.SKS  || ' | KODE MATKULIAH : ' || V_KRS.KODE || ' | NILAI : ' || V_KRS.NILAI);
            END LOOP;
END;